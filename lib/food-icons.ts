export function getSuggestedIcon(text: string): string | null {
    const lowerText = text.toLowerCase();

    // Check for exact matches first (optional, but good for performance)

    // Iterate through keywords
    for (const [icon, keywords] of Object.entries(ICON_KEYWORDS)) {
        for (const keyword of keywords) {
            // Check if the text contains the keyword as a whole word or part of it?
            // "yogur proteico" contains "yogur".
            // Simple includes check might be enough for now.
            if (lowerText.includes(keyword)) {
                return icon;
            }
        }
    }

    return null;
}
